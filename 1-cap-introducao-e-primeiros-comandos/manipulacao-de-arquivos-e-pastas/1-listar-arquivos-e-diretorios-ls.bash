ls

# O comando ls permite exibir informações dos arquivos e subdiretórios de um
# determinado diretório

ls -la

# O comando ls é geralmente executado com dois parâmetros muito úteis: “-l”,
# que exibe uma lista longa ou detalhada de informações, e o “-a”, que vem de all, ou
# seja, traga todos os arquivos e diretórios, mesmo aqueles que são ocultos