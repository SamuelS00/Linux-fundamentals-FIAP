# responsável pelo gerenciamento de pacotes em um sistema do tipo .deb

apt update

# O comando apresentado na Linha de comando # apt update atualiza a
# referência dos repositores, ou seja, ele baixa a lista de pacotes disponíveis para
# download com suas versões

apt upgrade

# O comando na Linha de comando # apt upgrade exibe a lista de pacotes que
# estão atualizados e confirma se desejamos realmente atualizá-los

apt search trezor

# O comando apt possui um parâmetro “search”, que permite buscar por
# pacotes antes de instalá-los.

